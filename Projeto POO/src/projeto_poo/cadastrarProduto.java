    private void btnCadManuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadManuActionPerformed
        //Manufatura, processo

        this.process.setNome(manuProcess.getText());
        this.process.setCustoHora(Float.parseFloat(this.manuCusto.getText()));

        this.manu.setProcesso(this.process);
        this.manu.setQtde(Integer.parseInt(this.ingredienteQtde.getText()));

        cadastrarProduto.listaManufatura.add(this.manu);

        JOptionPane.showMessageDialog(null, "Manufatura cadastrado com sucesso!");
    }//GEN-LAST:event_btnCadManuActionPerformed

    private void btncadIngrediActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncadIngrediActionPerformed
        //Produto

        this.produto.setNome(produtoNome.getText());

        //Ingredientes e materia prima
        this.matPrima.setNome(ingredienteNome.getText());
        this.matPrima.setFornecedor(ingredienteFornecedor.getText());
        this.matPrima.setUnidade(ingredienteUnidade.getText());
        this.ingrediente.setQtde(Integer.parseInt(ingredienteQtde.getText()));
        this.matPrima.setPreco(Float.parseFloat(ingredientePreco.getText()));

        JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso!");

    }//GEN-LAST:event_btncadIngrediActionPerformed

